    private void bntIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntIngresarActionPerformed
        if(this.txtIp.getText().isEmpty() || this.txtPuerto.getText().isEmpty() || this.txtBd.getText().isEmpty() || this.txtUser.getText().isEmpty()){
            int op = JOptionPane.showConfirmDialog(this, "Laa coneccion se realizara con los datos por defecto.\n"
                                            + "¿Desea continuar?", "¿Desea Continuar?", JOptionPane.YES_NO_CANCEL_OPTION);
            if(op == JOptionPane.OK_OPTION){
                if(BD.conectarBD()){
                  writeFile(config,this.txtDriver.getText()+"\n"+this.txtIp.getText()+"\n"+this.txtPuerto.getText()+"\n"+this.txtBd.getText()+"\n"+this.txtUser.getText()+"\n"+String.valueOf(this.pfPass.getPassword()));
                  iniciaClientes();
                }
            }else{
                this.txtBd.requestFocus();
            }
        }else{
            BD.setMyDriver(this.txtDriver.getText());
            BD.setIp(this.txtIp.getText());
            BD.setPuerto(this.txtPuerto.getText());
            BD.setBaseDatos(this.txtBd.getText());
            BD.setUsuario(this.txtUser.getText());
            if(this.pfPass.getPassword().length>0){
              BD.setPassworld(String.valueOf(this.pfPass.getPassword()));  
            }else{
                BD.setPassworld("");
            }           
            if(BD.conectarBD()){
                writeFile(config,this.txtDriver.getText()+"\n"+this.txtIp.getText()+"\n"+this.txtPuerto.getText()+"\n"+this.txtBd.getText()+"\n"+this.txtUser.getText()+"\n"+String.valueOf(this.pfPass.getPassword()));
                iniciaClientes();
            }
        }
    }//GEN-LAST:event_bntIngresarActionPerformed


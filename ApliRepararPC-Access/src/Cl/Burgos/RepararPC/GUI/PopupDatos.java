    private void verificarConfiguracion(){
        BD.getInstance();
        File fconfig = exitsFileConfig(config);
        if(fconfig != null){
            ArrayList rfc = readFileConfig(fconfig);
            if(rfc.size()>0){
                this.txtDriver.setText(rfc.get(0).toString());
                this.txtIp.setText(rfc.get(1).toString());
                this.txtPuerto.setText(rfc.get(2).toString());
                this.txtBd.setText(rfc.get(3).toString());
                this.txtUser.setText(rfc.get(4).toString());
                
                BD.setMyDriver(rfc.get(0).toString());
                BD.setIp(rfc.get(1).toString());
                BD.setPuerto(rfc.get(2).toString());
                BD.setBaseDatos(rfc.get(3).toString());
                BD.setUsuario(rfc.get(4).toString());
                
                if(rfc.size() > 5){
                   this.pfPass.setText(rfc.get(5).toString()); 
                   BD.setPassworld(rfc.get(5).toString());
                }else{
                    this.pfPass.setText("");
                }
                if(BD.conectarBD()){
                    int op = JOptionPane.showConfirmDialog(this, "El Sistema ha encontrado un archivo de configuracion."
                            + " y los datos son validos.\nRuta del Archivo:"
                            + fconfig.getAbsolutePath()+"\n"
                            + "¿Desea continuar? ", "¿Desea Continuar?", JOptionPane.YES_NO_OPTION);
                    if(op == JOptionPane.OK_OPTION){
                        iniciaClientes();
                    }else{
                        this.setVisible(true);
                    }
                }else{
                    this.setVisible(true);
                }
            }
            
       }else{
           this.setVisible(true);
       }
    }

